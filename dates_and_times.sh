# Dates and Times

# View clock with xclock
xclock -update 1 -geom 900x900
xclock -update 1 -digital
# -update          : how often the clock is updated in seconds
# -geom, -geometry : resolution of the clock
# -digital         : view digital clock

# View calender with cal
cal
cal -y
cal -y 2024
# -y      : current year's calender
# -y 2024 : calender of the year 2024 

# View dates and times with date
date

